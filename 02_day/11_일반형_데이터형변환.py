'''
    데이터 형변환
    int(값)
    float(값)
    bool(값)
    str(값)
    chr(값)
    ord(값)
'''
print(int(3.14)) # 3
print(int(-3.14)) # -3
print(int(True)) # 1
print(int(False)) # 0
print(int("123")+1) #124


print(float(3)) # 3.0
print(float(-3)) # -3.0
print(float(True)) #1.0
print(float(False)) #0.0
print(float(123.445)+1) #124.445


print(bool(0)) #True
print(bool(10)) #False


print(str(123)) # '123'
print(str(True)) # 'True'
print(str(4.13)) # '4.13'

print(chr(65), chr(97)) # A a
print(ord('A'), ord('a')) # 65 97



