"""
    리스트 색인
    m= [10,9,8,7]

    -4 -3 -2 -1(역방향)
    10  9  8  7
     0  1  2  3(순방향)
"""
#순방향
m = [10,9,8,7]
print(m[0]) #10
print(m[1]) # 9
print(m[2]) # 8
print(m[3]) # 7
#print(m[4]) #없는 위치 값 지정하면 IndexError 발생
#역방향
print()
print(m[-1]) # 7
print(m[-2]) # 8
print(m[-3]) # 9
print(m[-4]) # 10

#2. 슬라이싱

#순방향
print(m[0:3]) #[10, 9, 8]
print(m[:3]) #[10, 9, 8]
print(m[0:]) #[10, 9, 8, 7]
print(m[:]) #[10, 9, 8, 7]

#역방향
print(m[-4:-1]) #[10, 9, 8]

#step
print(m[::2]) #[10, 8]