"""
    튜플 색인 -> 색인은 전부 대괄호를 사용한다.
    m= [10,9,8,7]

    -4 -3 -2 -1(역방향)
    10  9  8  7
     0  1  2  3(순방향)
"""
m= ((1,2,3,),(4,5,6),(7,8,9))

#순방향
print(m[0]) #(1, 2, 3)
print(m[1]) #(4, 5, 6)
print(m[2]) #(7, 8, 9)

#역방향
print(m[-1]) #(7, 8, 9)
print(m[-2]) #(4, 5, 6)
print(m[-3]) #(1, 2, 3)

#5값 출력하기
print(m[1][1]) #5
print(m[-2][-2]) #5
print(m[1][-2]) #5
print(m[-2][1]) #5

#2. 슬라이싱
print(m[1][:2])

